        $(document).scroll(function(){
            var oft = $(".products-showBox").height()+$('.serviceFico').height();
            var sct = $(document).scrollTop();
            var cz = sct-oft;            
            if(cz >= 60)
            {
                $("#prMenuBox").css({"z-index":99999,"position":"fixed"});
            }
            else
            {
                $("#prMenuBox").css({"z-index":1,"position":"inherit"});
